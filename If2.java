package oop20230711;

public class If2 {

	public static void main(String[] args) {
		//단순if
		int number = 5;
		
		if(number < 0) {
			System.out.println("음수입니다.");
		}
		
		if(number > 0) {
			System.out.println("양수입니다.");
		}
		if(number == 0) {
			System.out.println("0입니다.");
		}
	}

}
